--- Write a solution to find the ids of products that are both low fat and recyclable.
Database Example:
Input: 
Products table:
+-------------+----------+------------+
| product_id  | low_fats | recyclable |
+-------------+----------+------------+
| 0           | Y        | N          |
| 1           | Y        | Y          |
| 2           | N        | Y          |
| 3           | Y        | Y          |
| 4           | N        | N          |
+-------------+----------+------------+
Output: 
+-------------+
| product_id  |
+-------------+
| 1           |
| 3           |
+-------------+

Solution:
SELECT name 
FROM customer 
WHERE referee_id != 2 OR referee_id IS NULL;
